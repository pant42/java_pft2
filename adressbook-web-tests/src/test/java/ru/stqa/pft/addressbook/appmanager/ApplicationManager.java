package ru.stqa.pft.addressbook.appmanager;

import org.openqa.selenium.*;
import org.openqa.selenium.chrome.ChromeDriver;

import java.util.concurrent.TimeUnit;

public class ApplicationManager {
  WebDriver wd;
  private NavigatorHelper navigatorHelper;
  private ContantHelper contantHelper;
  private GroupHelper groupHelper;
  private SessionHelper sessionHelper;
  JavascriptExecutor js;

  public void init() {
    wd = new ChromeDriver();
    wd.manage().timeouts().implicitlyWait(30, TimeUnit.SECONDS);
    js = (JavascriptExecutor) wd;

    wd.get("http://localhost/addressbook/");
    groupHelper = new GroupHelper(wd);
    contantHelper = new ContantHelper(wd);
    navigatorHelper = new NavigatorHelper(wd);
    sessionHelper = new SessionHelper(wd);
    sessionHelper.login("admin", "secret");
  }



  public void stop() {
    wd.quit();
  }

  public void logout() {
    wd.findElement(By.linkText("Logout")).click();
  }

  public boolean isElementPresent(By by) {
    try {
      wd.findElement(by);
      return true;
    } catch (NoSuchElementException e) {
      return false;
    }
  }

  public boolean isAlertPresent() {
    try {
      wd.switchTo().alert();
      return true;
    } catch (NoAlertPresentException e) {
      return false;
    }
  }

  public GroupHelper getGroupHelper() {
    return groupHelper;
  }

  public ContantHelper getContactHelper() {
    return contantHelper;
  }

  public NavigatorHelper getNavigatorHelper() {
    return navigatorHelper;
  }
}
